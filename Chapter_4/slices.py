# create a list and then slice for the first three items, the middle three items, and the last three items

flavors = ['chocolate', 'strawberry', 'vanilla', 'rocky road', 'butter pecan', 'fudge brownie', 'cookie dough']

print(f"The first three ice cream flavors are:")
print(flavors[0:3])

print(f"The middle three ice cream flavors are:")
print(flavors[2:5])

print(f"The last three ice cream flavors are:")
print(flavors[-3:])